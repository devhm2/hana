<br />
Để trang tr&iacute; văn bản cho đẹp, bạn cần sử dụng header and footer của văn bản cho tốt.&nbsp;
<h3><span style="color:#2ecc71">Chọn mẫu header and footer như thế n&agrave;o cho đẹp?</span></h3>

<ul>
  <li>M&agrave;u sắc của header, footer n&ecirc;n tu&acirc;n theo nhận diện thương hiệu của c&ocirc;ng ty. Bạn c&oacute; thể b&aacute;m v&agrave;o logo để chọn lựa m&agrave;u sắc cho ph&ugrave; hợp.</li>
  <li>Về đường n&eacute;t của header and footer th&igrave; cũng n&ecirc;n kết hợp giữa yếu tố nhận diện thương hiệu v&agrave; đường n&eacute;t mang t&iacute;nh chất đặc th&ugrave; ng&agrave;nh nghề của kh&aacute;ch h&agrave;ng. V&iacute; dụ, c&ocirc;ng ty của bạn kinh doanh về lĩnh vực li&ecirc;n quan đến nước th&igrave; bạn c&oacute; thể sử dụng c&aacute;c họa tiết hoặc ảnh mang t&iacute;nh chất nước để t&ocirc;n l&ecirc;n n&eacute;t đặc trưng ng&agrave;nh nghề.</li>
  <li>K&iacute;ch thước của header and footer n&ecirc;n h&agrave;i h&ograve;a, ph&ugrave; hợp với nhau. Bạn c&oacute; thể để k&iacute;ch thước header lớn hơn footer, chứ kh&ocirc;ng n&ecirc;n ngược lại.</li>
  <li>M&agrave;u sắc của header v&agrave; footer bạn n&ecirc;n đi theo hướng nhẹ nh&agrave;ng một ch&uacute;t để t&ocirc;n nội dung l&ecirc;n. Bởi d&ugrave; g&igrave; th&igrave; header v&agrave; footer chỉ mang t&iacute;nh chất trang tr&iacute;, l&agrave;m nổi bật cho nội dung l&ecirc;n m&agrave; th&ocirc;i.</li>
</ul>

